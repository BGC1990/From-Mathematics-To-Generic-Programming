class EuclidsAlgorithm{
    private: 
    int firstMeasure, secondMeasure;

    public:
    EuclidsAlgorithm(int a, int b) : firstMeasure(a), secondMeasure(b)
    { }

    int gcm0(int a, int b)
    {
        while(a != b)
        {
            if (b < a)
            {
                a = a - b;
            }
            else
            {
                b = b - a;
            }
        }
        return a;
    }
};

